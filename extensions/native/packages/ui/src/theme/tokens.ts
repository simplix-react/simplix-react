/**
 * Resolve the effective color token map for a preset and scheme.
 *
 * Mirrors the web CSS cascade: base light, then `.dark` overrides, then the
 * preset's light block, then the preset's dark block. `var(--x)` references
 * (e.g. `--brand-bg: var(--primary)`) are resolved after merging so preset
 * primaries flow into dependent tokens.
 */
export function resolveThemeTokens(
  preset: ThemePresetName,
  scheme: "light" | "dark",
): ColorTokenMap {
  const merged: ColorTokenMap = { ...BASE_TOKENS.light };
  if (scheme === "dark") Object.assign(merged, BASE_TOKENS.dark);
  if (preset !== "default") {
    const presetSheet = PRESET_TOKENS[preset];
    Object.assign(merged, presetSheet.light);
    if (scheme === "dark") Object.assign(merged, presetSheet.dark);
  }
  // Resolve var() references (bounded — reference chains are at most a few deep).
  for (let pass = 0; pass < 4; pass++) {
    let changed = false;
    for (const [key, value] of Object.entries(merged)) {
      const ref = value.match(/^var\((--[a-z0-9-]+)\)$/);
      if (ref) {
        const target = merged[ref[1]];
        if (target !== undefined && target !== value) {
          merged[key] = target;
          changed = true;
        }
      }
    }
    if (!changed) break;
  }
  return merged;
}
