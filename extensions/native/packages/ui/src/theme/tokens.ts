/**
 * Color token sheet mirroring the web theme (@simplix-react/ui theme/base.css
 * and theme/presets/*.css) with the SAME token names, converted from oklch to
 * hex for the React Native style engine. Values are injected as CSS variables
 * via NativeWind `vars()` by {@link SimplixThemeProvider}; components consume
 * them only through Tailwind utilities mapped in the package tailwind preset
 * (e.g. `bg-background`, `text-foreground`, `border-border`).
 */

/** A flat map of CSS variable name to color value (hex / hex8 / var reference). */
export type ColorTokenMap = Record<string, string>;

/** Light/dark pair of color token maps. */
export interface ThemeTokenSheet {
  light: ColorTokenMap;
  dark: ColorTokenMap;
}

/** Color preset identifiers — mirrors the web `THEMES` manifest values. */
export type ThemePresetName = "default" | "blue" | "green" | "krds" | "orange" | "rose" | "violet";

